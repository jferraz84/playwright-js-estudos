//Relational or comparison operators
// >, <, >=, <=, ==, ===, !=, !==

// > - more than (maior que)
// < - less than (menor que)
// >= - more than or equal to (maior ou igual a)
// <= - less than or equal to (menor ou igual a)

var result1 = 5 > 3; //true
console.log("5 > 3: " + result1);
console.log("3 > 5: " + (3 > 5));
console.log("5 >= 5: " + (5 >= 5));
console.log("4 <= 5: " + (4 <= 5));

// == - equal to (loose equality) (igual a - comparação frouxa)
// === - equal to (strict equality) (igual a - comparação estrita)
// != - not equal to (loose inequality) (diferente de - comparação frouxa)
// !== - not equal to (strict inequality) (diferente de - comparação estrita)   

console.log("5 == '5': " + (5 == '5')); 
console.log("5 === '5': " + (5 === '5'));
console.log("5 != '5': " + (5 != '5')); 
console.log("5 !== '5': " + (5 !== '5'));   